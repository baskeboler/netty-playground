package com.victor.commandserver.server.commands;

public class ComandoSumar {
	int a;
	int b;
	
	public ComandoSumar(int a, int b) {
		this.a = a;
		this.b = b;
	}
	public int getA() {
		return a;
	}
	public void setA(int a) {
		this.a = a;
	}
	public int getB() {
		return b;
	}
	public void setB(int b) {
		this.b = b;
	}
	
}
